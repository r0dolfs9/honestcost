window.CAR_INDEX = {
  byId:    Object.fromEntries(window.CAR_DB.map(c => [c.id, c])),
  byBrand: window.CAR_DB.reduce((acc,c) => {
    (acc[c.brand] = acc[c.brand] || []).push(c);
    return acc;
  }, {}),
  brands:  [...new Set(window.CAR_DB.map(c => c.brand))].sort(),
  total:   window.CAR_DB.length,
};

/* ─── Search helper ───────────────────────────────────────── */
window.searchCars = function(query, limit = 12) {
  const q = (query || '').toLowerCase().trim();
  if (!q) return [];
  return window.CAR_DB.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.id.includes(q) ||
    c.brand.toLowerCase().includes(q)
  ).slice(0, limit);
};
